function startTranslation(text, date) {
    
    splitContentToArray(text, date);
    
}

function splitContentToArray(text, date) {
    
    var splittedContent = text.split(" ");
    prepareTranslation(splittedContent, date);
    
}

function prepareTranslation(textArray, date) {
    
    var output = "";
    
    for (var index=0;textArray.length>index;index++){
        
        output += " " + makeTranslation(textArray[index]);
        
        if (index + 1 == textArray.length){
            showOutput(output, date);
        }
    }
    
}

function makeTranslation(text){
    var index = 0;
    
    for (var key in translatorContent){

        if (text == key) {
            return translatorContent[key];
        } else {

        }
        
        if (index==Object.keys(translatorContent).length - 1){
            /*Returns to the bottom of the for-loop, and found no translation.
            Therefore it will return the input text yet again. This method
            is more efficient instead of just returning nothing or 'undefined'.*/
            return text;
        }
        
        index++;
    }
}

function showOutput(translation, date) {
    document.getElementById("sidemal").value = translation;
    document.getElementById("secDesc").innerHTML = "Oversatte tekst på " + date.getMilliseconds() + " millisekunder.";
}